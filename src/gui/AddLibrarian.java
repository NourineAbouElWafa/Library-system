    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = LibrarianName.getText();
        String password = PasswordField2.getText();
        String email = Email.getText();
        String address = Address.getText();
        String city = City.getText();
        String contactNo = Contact.getText();
        boolean found = false;

        if (name.isEmpty() || !isAlphabet(name)) {
            JOptionPane.showMessageDialog(this, "please enter a valid name");

        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter a password");
        } else if (email.isEmpty() || !checkemail(email)) {
            JOptionPane.showMessageDialog(this, "please enter a valid e-mail ");

        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter the address of the librarian");
        } else if (city.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter the city of the librarian");
        } else if (contactNo.isEmpty() || !isNumeric(contactNo)) {
            JOptionPane.showMessageDialog(this, "please enter a valid contact");

        } else {
            try {
                int num = Integer.parseInt(contactNo);
                Librarian.readFile();
                if (Librarian.FoundLibrarian(name, password)) {
                    JOptionPane.showMessageDialog(this, "Librarian Already exists");

                } else {
                    Librarian.AddLibrarian(name, password, email, address, city, num);
                    JOptionPane.showMessageDialog(this, "The librarian was added successfully");
                }
            } catch (FileNotFoundException | ParseException ex) {
                Logger.getLogger(AddLibrarian.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

