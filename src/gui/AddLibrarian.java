    public static boolean isNumeric(String s) {
        for (char c : s.toCharArray()) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }
    // method to check uf a string has only letters

    public static boolean isAlphabet(String s) {
        for (char c : s.toCharArray()) {
            if (!Character.isLetter(c) && !Character.isSpace(c)) {
                return false;
            }
        }
        return true;
    }

    // method to check if email address is valid
    public static boolean checkemail(String s) {
        int i = 0;
        int atflag = 0;
        int flagIndex = -1;
        int dotIndex = -1;

        for (char c : s.toCharArray()) {
            if (c == '@') {
                atflag++;
                flagIndex = i;
            }
            if (c == '.') {
                dotIndex = i;
            }
            i++;
        }
        if (atflag > 1 || atflag == 0 || (dotIndex - flagIndex) < 2) {
            return false;
        }
        return true;
    }

